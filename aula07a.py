v1 = int(input('Digite um valor: '))
v2 = int(input('Digite outro valor '))
# Operção matemáticas
s = v1 + v2
m = v1 * v2
d = v1 / v2
di = v1 // v2
redi = v1 % v2
e = v1 ** v2
print('-----------------------')
#Resultados das operção a cima:
print('\n a soma é: {} \n o produto é: {} \n a divisão: {:.3f} \n a divisão inteira é: {}'.format(s,m,d,di))
print(' o resto da divisão é: {} \n e a exponenciação é: {}'.format(redi,e))

